    private String[] loadTitleList() {
        String[] lista = new String[db.getAllElements().size()];

        for (int i = 0; i < lista.length; i++) {
            lista[i] = db.getAllElements().get(i).getTitulo();
        }

        jList1.setListData(lista);//cargo los titulos a la lista

        return lista;
    }

    private void loadNotaUI(String titulo, String contenido) {
        jTextField1.setText(titulo);
        jTextArea1.setText(contenido);
    }


public class DesicionIf{//comienza la clase

    public static void main(String[] args){//inicia método main
        int v1 = 3;
        int v2 = 5;

        if(v1 == v2){//comienza if
            System.out.println(v1 +" es igual a " + v2);
        }//termina if
        if(v1 < v2){//comienza if
            System.out.println(v1 +" es menor a " + v2);
        }//termina if
        if(v1 > v2){//comienza if
            System.out.println(v1 +" es mayor que a " + v2);
        }//termina if
        if(v1 >= v2){//comienza if
            System.out.println(v1 +" es mayor que o igual a " + v2);
        }//termina if
        if(v1 <= v2){//comienza if
            System.out.println(v1 +" es menor que o igual a " + v2);
        }//termina if
        if(v1 != v2){//comienza if
            System.out.println(v1 +" es diferente a " + v2);
        }//termina if

    }//termina método main
}//termina clase main
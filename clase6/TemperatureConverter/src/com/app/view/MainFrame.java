    private void calculo() {
        String result = fieldValue.getText();
        if(result.isEmpty()){
            result = "0";
            fieldValue.setText("0");
        }
        double r = Double.parseDouble(result);
        String unit = "";

        switch (comboOptions.getSelectedIndex()) {
            case 0://Celsius
                if (radioOption1.isSelected()) {
                    r = Temperature.celsiusToFahrenheit(r);
                    unit = Unit.FAHRENHEIT;
                } else {
                    r = Temperature.celsiusToKelvin(r);
                    unit = Unit.KELVIN;
                }
                break;
            case 1://Fahrenheit
                if (radioOption1.isSelected()) {
                    r = Temperature.fahrenheitToCelsius(r);
                    unit = Unit.CELSIUS;
                } else {
                    r = Temperature.fahrenheitToKelvin(r);
                    unit = Unit.KELVIN;
                }
                break;
            case 2://kelvin
                if (radioOption1.isSelected()) {
                    r = Temperature.kelvinToCelsius(r);
                    unit = Unit.CELSIUS;
                } else {
                    r = Temperature.kelvinToFahrenheit(r);
                    unit = Unit.FAHRENHEIT;
                }
                break;
        }

        labelResult.setText(String.format("%.2f %s", r, unit));
    }
